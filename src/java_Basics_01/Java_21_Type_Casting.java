package java_Basics_01;

public class Java_21_Type_Casting {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(a);

        int b = (int) 0.5;
        System.out.println(b);

        double c = 12.0;
        System.out.println(c);

        int d = (int) c;
        System.out.println(d);


        /*
        byte
        short
        char
        int
        long
        float
        double
         */
    }
}
