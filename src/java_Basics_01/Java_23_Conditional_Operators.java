package java_Basics_01;

public class Java_23_Conditional_Operators {
    public static void main(String[] args) {
        int a = 7 > 3 ? 7 : 3;
        System.out.println(a);

        String hello = "hello";

        double result = hello.equals("Hello") ? 1 : 0;
        System.out.println(result);

    }
}
